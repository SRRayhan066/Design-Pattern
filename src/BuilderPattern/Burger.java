package BuilderPattern;

public class Burger {
    private int meat;
    private int cheese;
    private int letus;

    public int getMeat() {
        return meat;
    }

    public void setMeat(int meat) {
        this.meat = meat;
    }

    public int getCheese() {
        return cheese;
    }

    public void setCheese(int cheese) {
        this.cheese = cheese;
    }

    public int getLetus() {
        return letus;
    }

    public void setLetus(int letus) {
        this.letus = letus;
    }
}
